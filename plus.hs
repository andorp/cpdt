module Plus where

import qualified Prelude

data Nat =
   O
 | S Nat

nat_rect :: a1 -> (Nat -> a1 -> a1) -> Nat -> a1
nat_rect f f0 n =
  case n of {
   O -> f;
   S n0 -> f0 n0 (nat_rect f f0 n0)}

nat_rec :: a1 -> (Nat -> a1 -> a1) -> Nat -> a1
nat_rec =
  nat_rect

plus_rec :: Nat -> Nat -> Nat
plus_rec =
  nat_rec (\m -> m) (\n r m -> S (r m))

